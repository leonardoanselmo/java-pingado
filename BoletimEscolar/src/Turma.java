
public class Turma {
	
	private String nome;
	private String turno;
	
	public String getNome(){
		return this.nome;
	}
	
	public void setNome(String nome){
		this.nome = nome;
	}

	public String getTurno(){
		return this.turno;
	}
	
	public void setTurno(String turno){
		this.turno = turno;
	}
	
}
